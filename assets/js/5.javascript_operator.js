// Operator

// Aritmatika
let a = 10;
let b = 3;

console.log(a+b);
console.log(a-b);
console.log(a*b);
console.log(a/b);
console.log(a%b);
console.log(a**b);

// Assignment
let c = 2;
console.log(c);
c+=5;
console.log(c);

// Comparison
let d = 4;
let e = 5;

console.log(d == e); // false
console.log(d < e); // true
console.log(d > e); // false
console.log(d != e); // true

// Logical
let f = true;
let g = false;

console.log(f && g);
console.log(f || g);
console.log(!f);

// Bitwise
let h = 00111100;
let i = 00001101;

console.log(h & i);
console.log(h | i);
console.log(h ^ i);
console.log(~h);

// Ternary
let j = 1;
let k = 1;

console.log(j == k ? "yes" : "no"); // hasil "yes"
